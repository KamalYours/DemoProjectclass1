public class TypeWrap {
    public static void main(String[] args) {


        char c = 'x';
        int i = 25;
        byte b = 55;
        short s = 5246;
        long l = 2121212121212l;
        float f1 = 3.1212f;
        double d2 = 0.00004545;

        System.out.println("C ="+c);
        System.out.println("i ="+i);
        System.out.println("b ="+b);
        System.out.println("s="+s);
        System.out.println();
        System.out.println();
    }

}
