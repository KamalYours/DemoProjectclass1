public class JavaMathExample {

    public static void main(String[] args) {

        double x=28;
        double y=4;

        System.out.println("Maximum number of x and y:,,,"+Math.max(x,y));
        System.out.println("Minimum number of x and y:,,,,"+Math.min(x,y));
        System.out.println("Square root of x ,,,"+Math.sqrt(x));
        System.out.println("Square root of the y,,,"+Math.sqrt(y));
        System.out.println("Power of x and y,,,,"+Math.pow(x ,y));
    }
}
