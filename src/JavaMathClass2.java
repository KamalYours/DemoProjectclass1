public class JavaMathClass2 {

    public static void main(String[] args) {

        double x = 30;

        System.out.println("Sine value of x.."+Math.sin(x));
        System.out.println("Cosine value of x..."+Math.cos(x));
        System.out.println("Tangent value of x ..."+Math.tan(x));
        System.out.println("Sine value of x .."+Math.asin(x));
        System.out.println("Cosine value of x.."+Math.acos(x));
        System.out.println("Tangent value of x... +M" +Math.atan(x));
        System.out.println("Sine value of x .."+Math.sinh(x));
        System.out.println("Cosine value of x ..."+Math.cosh(x));
        System.out.println("Tangent value of x ..."+Math.tanh(x));
    }
}
